import express from "express";
import {
  registerUser,
  loginUser,
  getUserProfile,
  updateUserProfile,
  updateUserPassword,
  addAddress,
  removeAddress,
  addToWishlist,
  removeFromWishlist,
  getAllUsers,
  getUserById,
  updateUser,
  deleteUser,
  trackBrowsingHistory,
  getBrowsingHistory,
  logoutUser
} from "../controllers/userController.js";
import {admin} from "../middleware/authMiddleware.js";
import { protect } from "../middleware/authMiddleware.js";
import { checkRole } from "../middleware/adminMiddleware.js";

